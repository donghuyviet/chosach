     */
    public function store(UserRequest $request)
    {
        $user = new User;
        $user->name = $request->txtUser;
        $user->email= $request->txtEmail;
        $user->password = Hash::make($request->txtPass).'hello';
        $user->save();      
        return redirect()->route('user.index');
    }

